package core_java_new;
public class Arithmatic_Operators 
{
	static void add()
	{
		int a=100;
		int b=110;
		int sum=a+b;
		System.out.println(sum);
	}
	static void sub()
	{
		int a=100;
		int b=110;
		int sub=a-b;
		System.out.println(sub);
	}
	static void multi()
	{
		int a=10;
		int b=11;
		int mul=a*b;
		System.out.println(mul);
	}
	static void divide()
	{
		int a=30;
		int b=10;
		int div=a/b;
		System.out.println(div);
	}
	static void modu()
	{
		int a=25;
		int b=15;
		int mod=a%b;
		System.out.println(mod);
	}
	
	public static void main(String[] args) 
	{
		add();
		sub();
		multi();
		divide();
		modu();
		
		
	}

}
