package core_java_new;
public class GB_Var_5staticM 
{
	
	   static  int a=100;          //Global Variable
	    static  int b=200;

	     static void add()
	     {
	    	 
		       System.out.println(a+b);
	     }
	     static void sub()
	     {
		       System.out.println(b-a);
	     }
	     static void multi()
	     {
		       System.out.println(a*b);
	     }
	     static void divide()
	     {
		       System.out.println(b/a);
	     }
	     static void modu()
	     {
		        System.out.println(a%b);
	     }
	     
	     
	public static void main(String[] args) 
	{
		
		          add();
		          sub();
		          multi();
		          divide();
		          modu();
		 
		
		
		
		
	
	}

}
