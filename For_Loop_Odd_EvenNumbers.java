package core_java_new;
public class For_Loop_Odd_EvenNumbers
{
    public static void main(String[] args)
    {
    	for(int i=1;i<=50;i++)
    	{
    		System.out.println(i);         //Odd no.
    		i++;
    	}

    //*************************************************************

    	for(int i=-1;i>=-50;i--)
    	{
    		System.out.println(i);         //Odd no. in minus
    		i--;
    	}

    //******************************************************

    	for(int i=2;i<=50;i++)
    	{                                       //even no.
    		System.out.println(i);
    		i++;
    	}

    //*************************************************************

    	for(int i=-2;i>=-50;i--)
    	{                                       //even no. in minus
    		System.out.println(i);
    		i--;
    	}





    }

}
