package core_java_new;
public class For_Loop_PrintName
{
	public static void main(String[] args)
	{

		for(int i=0;i<2;i++)
		{
			System.out.println("Choudhary Ji");
		}

	//*************************************************

		for(int i=0;i<=2;i++)
		{
			System.out.println("Choudhary sahab");
		}

 //*************************************************************

		for(int i=1;i<11;i++)
		{
			System.out.println("Ajit Jat");
		}

 //********************************************************

		for(int i=-1;i<=10;i++)
		{
			System.out.println(i);
		}







	}

}
