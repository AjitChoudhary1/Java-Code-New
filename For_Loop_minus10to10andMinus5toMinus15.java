package core_java_new;
public class For_Loop_minus10to10andMinus5toMinus15
{
	public static void main(String[] args)
	{

		for(int i=-10;i<=10;i++)
		{
			System.out.println(i);
		}

	//********************************************

		for(int i=-5;i>-16;i--)
		{
			System.out.println(i);
		}







	}

}
