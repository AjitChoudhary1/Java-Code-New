package core_java_new;
public class Dec_Inci_Datatype
{
	public static void main(String[] args)
	{
        byte a=80;                     // Declaration and Initialization
		System.out.println(a);         // utilization

		short b=31;
		System.out.println(b);

		int c=123;                // Declaration and Initialization
		System.out.println(c);

		int c1;                      // Declaration
		c1=321;                      // Initialization
		System.out.println(c1);     // utilization

		long d=959985;
		System.out.println(d);

		float d1=20.31f;
		System.out.println(d1);

		double e=314.2564;
		System.out.println(e);

		char f='A';
		System.out.println(f);

		char g='M';
		System.out.println(g);

		boolean h=true;
		System.out.println(h);

		String i="Ajit Jat";
		System.out.println(i);


	}

}
